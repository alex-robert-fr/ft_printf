#include "libft.h"

size_t	ft_memlen(unsigned long int nbr)
{
	int	i;

	i = 0;
	while (nbr >= 16)
	{
		nbr /= 16;
		i++;
	}
	if (nbr > 0)
		i++;
	return (i);
}
